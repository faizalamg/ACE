def add_two_numbers(a, b):
    """
    Simple function that adds two numbers.

    Args:
        a: First number (int or float)
        b: Second number (int or float)

    Returns:
        Sum of a and b
    """
    return a + b


def multiply_two_numbers(a, b):
    """
    Simple function that multiplies two numbers.

    Args:
        a: First number (int or float)
        b: Second number (int or float)

    Returns:
        Product of a and b
    """
    return a * b